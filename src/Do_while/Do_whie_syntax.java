package Do_while;

public class Do_whie_syntax {
    public static void main(String[] args) {
        //do while syntax firt intialize then body will execute atleast once then condition then updation
        int i = 0;
        do {
            System.out.println("this is the body whihc will execute 1 time");
            System.out.println(i);
            i++;

        }while(i<10);
    }
}